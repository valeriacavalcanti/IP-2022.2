n1 = int(input("Número 1: "))               # 10
n2 = int(input("Número 2: "))               # 3

soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao_real = n1 / n2
divisao_inteira = n1 // n2
resto = n1 % n2
potencia = n1 ** n2

print('Soma = ', soma)                      # 13
print('Subtração =', subtracao)             # 7
print('Multiplicação =', multiplicacao)     # 30
print('Divisão real =', divisao_real)       # 3.333333333
print('Divisão inteira =', divisao_inteira) # 3
print('Resto =', resto)                     # 1
print('Potência =', potencia)               # 1000
